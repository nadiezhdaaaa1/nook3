type SocialIconProps = {
  href?: string;
  label: string;
  disabled?: boolean;
  children: ReactNode;
};

export function SocialIcon({ href, label, disabled, children }: SocialIconProps) {
  const baseClasses =
    "focus-ring inline-flex items-center justify-center w-10 h-10 rounded-md transition-opacity";

  if (disabled || !href) {
    return (
      <span
        className={cn(baseClasses, "opacity-35 cursor-not-allowed")}
        aria-disabled="true"
        title={`${label} — coming soon`}
      >
        {children}
      </span>
    );
  }

  return (
    <a
      href={href}
      target="_blank"
      rel="noopener noreferrer"
      aria-label={label}
      className={cn(baseClasses, "opacity-70 hover:opacity-100")}
    >
      {children}
    </a>
  );
}
